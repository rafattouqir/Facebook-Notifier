private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String get = JOptionPane.showInputDialog(rootPane, "Enter id\n This is found in the Facebook notification center via the RSS-option and its GET string");
    if (get == null || get.trim().isEmpty()) {
        return;
    }
    int startId = get.indexOf("id=")+3;
    int endId = get.indexOf("&",startId);
    String id = get.substring(startId,endId);
    
    int startKey = get.indexOf("key=")+4;
    int endKey = get.indexOf("&",startKey);
    String key = get.substring(startKey,endKey);
    JOptionPane.showMessageDialog( rootPane, "Your id: "+ id +"\nYour key: " + key + "\nYour account was added" );
    FacebookAccount fa = new FacebookAccount(id, key);
    accounts.add(fa);
    listModel.addElement(fa);
}//GEN-LAST:event_jButton1ActionPerformed

    
