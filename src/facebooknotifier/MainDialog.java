private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
    String id = JOptionPane.showInputDialog(rootPane, "Enter id\n This is found in the Facebook notification center via the RSS-option and its GET string");
    if (id == null || id.trim().isEmpty()) {
        return;
    }
    String key = JOptionPane.showInputDialog(rootPane, "Enter key");
    if (key == null || key.trim().isEmpty()) {
        return;
    }
    FacebookAccount fa = new FacebookAccount(id, key);
    accounts.add(fa);
    listModel.addElement(fa);
}//GEN-LAST:event_addButtonActionPerformed

