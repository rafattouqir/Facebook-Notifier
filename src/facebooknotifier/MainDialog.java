    public MainDialog(final FacebookChecker facebookChecker) {
        this.listModel = new DefaultListModel<FacebookAccount>();
        accounts = facebookChecker.getAccounts();
        for (FacebookAccount facebookAccount : accounts) {
            listModel.addElement(facebookAccount);
        }
        initComponents();
        callpath = callPathField.getText();
        URL imgpath = ClassLoader.getSystemResource("facebooknotifyer/favicon.png");
        Image facebookIco = Toolkit.getDefaultToolkit().getImage(imgpath);
        tray = new TrayIcon(facebookIco, "Facebook Notifier");
        tray.setImageAutoSize(true);
        
        PopupMenu popupMenu = new PopupMenu("Menu");
     
        MenuItem openDialogItem = new MenuItem("Options");
        openDialogItem.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                setVisible(true);
            }
        });
        popupMenu.add(openDialogItem);
        
        
        MenuItem exitItem = new MenuItem("Exit");
        exitItem.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                System.exit(0);
            }
        });
        popupMenu.add(exitItem);
        
        tray.setPopupMenu(popupMenu);
        try {
            SystemTray.getSystemTray().add(tray);
        } catch (AWTException ex) {
            Logger.getLogger(MainDialog.class.getName()).log(Level.SEVERE, "Tray not supported", ex);
        }
        
        n = new Notifyer() {
            @Override
            public void onEvent(String title, String message) {
                tray.displayMessage(title, message, TrayIcon.MessageType.WARNING);
                try {
                    String call = String.format(callpath, message);
                    Runtime.getRuntime().exec(call);
                    Toolkit.getDefaultToolkit().beep();
                    System.out.println(message);
                    tray.setToolTip(message);
                } catch (Exception ex) {
                    //Logger.getLogger(MainDialog.class.getName()).log(Level.INFO, "Bad callpath entered", ex);
                }
            }

            @Override
            public void onCheck(String time) {}

            @Override
            public void onStatusChange(StatusType status) {}
        };
        
        FacebookChecker checker = new FacebookChecker(accounts);
        checker.addNotifyer(n);
    }

